def get_parameter(option):
    options = ["protein", "protein_CH", "protein_CHN", "drug", "drug_CH", "drug_CHN", "drug_CHO", "drug_CHNO"]
    if option == "protein":
        return get_protein_parameters()
    elif option == "protein_CH":
        return get_CH_atomtyped_protein_parameters()
    elif option == "protein_CHN":
        return get_CHN_atomtyped_protein_parameters()
    elif option == "drug":
        return get_drug_benchmark_parameters()
    elif option == "drug_CH":
        return get_drug_CH_benchmark_parameters()
    elif option == "drug_CHN":
        return get_drug_CHN_benchmark_parameters()
    elif option == "drug_CHO":
        return get_drug_CHO_benchmark_parameters()
    elif option == "drug_CHNO":
        return get_drug_CHNO_benchmark_parameters()
    elif option == "drug_C":
        return get_drug_C_benchmark_parameters()
    elif option == "drug_H":
        return get_drug_H_benchmark_parameters()
    elif option == "drug_H2":
        return get_drug_H2_benchmark_parameters()
    else:
        raise ValueError(
            f"Parameter not found for {option}. Supported options are {options}"
        )

def find_protein_parameters(typed_atom):
    if typed_atom == None:
        return  get_protein_parameters()
    elif typed_atom == [1,6]:
        return get_CH_atomtyped_protein_parameters()
    elif typed_atom == [1,6, 7]:
        return get_CHN_atomtyped_protein_parameters()
